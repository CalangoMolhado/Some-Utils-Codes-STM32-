void HAL_ADC_ConvCpltCallback(ADC_HandleTypeDef* hadc)
{
	mediaCH1=medidas[0][1];
	mediaCH0=medidas[0][0];
	for(int i=1;i<(AMOSTRAS/CANAIS);i++)
	{
		mediaCH1+=medidas[i][1];
		mediaCH0+=medidas[i][0];
	}
	mediaCH0/=(AMOSTRAS/CANAIS);
	valorCH0=mediaCH0*ESCALA;

	mediaCH1/=(AMOSTRAS/CANAIS);
	valorCH1=mediaCH1*ESCALA;

	corrente=(valorCH1*100)*1.06;//mA
	tensaoload=((valorCH0*9.45)-valorCH1);//V
	potencia=(corrente*tensaoload)/1000;//W
	resistencia=(tensaoload/corrente)*1000;//Ohms
}
